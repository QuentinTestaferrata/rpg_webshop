<p>This is my RPG web shop, I've chosen this since it would be something i would like creating, which would keep me motivated.<br>
If you have a web extension for dark mode, disable it, because it messes up the colors and backgrounds.<br>
Try visiting the website by reading each point, and trying out the functionality's then proceeding to the next point.<br><br>

Explanation of my project:<br><br>
<strong>1) Home page / item shop:</strong><br> On the home page we can see a list of all the items available in the shop.<br>
Users can buy these items, and admins can edit and/or create new items.<br>
You can press the "home" button or my logo on the navbar to redirect to this page.<br><br>

<strong>2) Profile page:<br></strong>
this is a basic profile page, which show the username, email, birthday and bio.<br>
This is editable, and users only see the email adress of their own profile, not when visiting other profiles.<br>
To edit your profile picture, just press edit, press the profile picture once, choose your picture and press save!<br><br>

<strong>3) Inventory:<br> </strong>
When buying an item, it updates your pouch $ and automatically goes into your inventory<br>
where you have an overview of all of your items.<br>
If empty, a message will tell you you have to buy something from the shop<br>
A bug i couldn't fix is going for the profile page to the inventory is impossible. Anywhere else works.<br><br>

<strong>4) Adventures / Quests: </strong><br>
If you want more money to buy stuff, there's a Quest feature, you can access this page via the navbar "adventures".<br>
Here you will see all of the possible jobs you can fullfil. There is no real game-mechanic. It's just like sending your troups on<br>
a quest in a game and waiting till your character comes back. You can see your active quests by clicking "go to your quests" pr via<br>
the navbar pressing on your name then "my quests". Try accepting the short quests (1 & 5 mins) while visiting the rest of the site,
and come back to claim the rewards after!<br>
Admins can create or delete quests.
<br><br>

<strong>5) FAQ: </strong><br>
Users can read some frequently asked questions, divided into categories.<br>
Admins can delete/edit/add Categories & delete/edit/add questions and answers.
<br><br>

<strong>6) Search friends: </strong><br>
The search bar in the middle of the navbar let's you look for other accounts.<br>
Try looking for "ganda", it will give all of the accounts starting with that. (white & gray gandalf)<br>
Pressing their names will lead you to their profile.
<br><br>

<strong>7) Contact:</strong><br> 
Admins: have a "Contact panel" accessible by clicking your name in the navbar.<br>
Here you can see all of the answered and unanswered questions. An admin can delete questions and respond to them.<br>
Users: Can ask questions, and see their statuses and/or responses to the questions asked. And also delete them if they want to.
<br><br>

<strong>8) User List:</strong><br>
Admins also have access to a user list found in the navbar dropdown list by clicking on their names.<br>
Here they see all of the user info, a link to the user's profile, and also promote someone to admin or remove their admin role.<br>
They can also delete a non-admin account.
<br><br><br>

